/// Obtient le nom traduit d'un skill selon la langue du système
String getSkillName(int skillId) {
  // Récupère le nom chinois du skill
  final chineseName = skillNames[skillId];
  if (chineseName == null) {
    return skillId.toString();
  }

  // Si la langue actuelle est chinoise, retourne le nom chinois
  final lang = TranslationService().currentLanguage;
  if (lang == 'zh') {
    return chineseName;
  }

  // Sinon, cherche la traduction anglaise
  final translation = skillTranslations[chineseName];
  if (translation != null) {
    return translation;
  }

  // Si pas de traduction trouvée, retourne le nom chinois
  return chineseName;
}
